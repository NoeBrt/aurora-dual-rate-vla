def render_cinematic_clutter(task: str) -> str:
    seed = {"handoff": 11, "clutter_tool": 29, "drawer_tool": 43, "language": 71}[task]
    rng = random.Random(seed)
    parts = []
    colors = ["#94a3b8", "#64748b", "#facc15", "#a3a3a3", "#f97316", "#475569"]
    for idx in range(24):
        x = rng.uniform(220, 640)
        y = rng.uniform(245, 610)
        cx, cy = iso(x, y)
        w = rng.uniform(22, 64)
        h = rng.uniform(10, 34)
        rot = rng.uniform(-28, 28)
        color = colors[idx % len(colors)]
        opacity = 0.62 if idx % 5 else 0.86
        parts.append(f'<rect x="{cx:.1f}" y="{cy:.1f}" width="{w:.1f}" height="{h:.1f}" rx="5" fill="{color}" stroke="#111827" opacity="{opacity}" transform="rotate({rot:.1f} {cx:.1f} {cy:.1f})"/>')
    return "\n".join(parts)


