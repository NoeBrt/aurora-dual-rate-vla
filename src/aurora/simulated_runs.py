def iso(x: float, y: float) -> tuple[float, float]:
    return (335 + 0.78 * x + 0.48 * y, 118 + 0.27 * x + 0.58 * y)


def render_floor_grid() -> str:
    lines: list[str] = []
    for offset in range(-4, 9):
        x1, y1 = iso(70 + offset * 86, 95)
        x2, y2 = iso(70 + offset * 86, 760)
        lines.append(f'<line x1="{x1:.1f}" y1="{y1:.1f}" x2="{x2:.1f}" y2="{y2:.1f}" stroke="#64748b" stroke-width="1" opacity="0.18"/>')
    for offset in range(0, 9):
        x1, y1 = iso(80, 130 + offset * 78)
        x2, y2 = iso(860, 130 + offset * 78)
        lines.append(f'<line x1="{x1:.1f}" y1="{y1:.1f}" x2="{x2:.1f}" y2="{y2:.1f}" stroke="#94a3b8" stroke-width="1" opacity="0.14"/>')
    lines.append('<ellipse cx="965" cy="318" rx="290" ry="190" fill="url(#lamp)" opacity="0.55"/>')
    return "\n".join(lines)


